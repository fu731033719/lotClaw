export function createModelAdapter(options: ModelAdapterOptions = {}): ModelAdapter {
  const provider = options.provider ?? "stub";

  if (provider === "stub") {
    return new StubModelAdapter();
  }

  if (provider === "openai") {
    if (!options.apiKey) {
      throw new Error('OPENAI_API_KEY is required when MODEL_PROVIDER is "openai".');
    }

    return new OpenAIModelAdapter({
      apiKey: options.apiKey,
      model: options.model ?? "gpt-5.4",
      baseURL: options.baseURL
    });
  }

  if (provider === "minimax") {
    if (!options.apiKey) {
      throw new Error('MINIMAX_API_KEY is required when MODEL_PROVIDER is "minimax".');
    }

    return new MiniMaxModelAdapter({
      apiKey: options.apiKey,
      model: options.model ?? "MiniMax-M2.5",
      baseURL: options.baseURL ?? "https://api.minimax.io/v1"
    });
  }

  if (provider === "qwen") {
    if (!options.apiKey) {
      throw new Error('QWEN_API_KEY is required when MODEL_PROVIDER is "qwen".');
    }

    return new QwenModelAdapter({
      apiKey: options.apiKey,
      model: options.model ?? "qwen-plus",
      baseURL: options.baseURL ?? "https://cn-hongkong.dashscope.aliyuncs.com/compatible-mode/v1"
    });
  }

  throw new Error(`Unsupported model provider "${provider}".`);
}

function buildResponseInput(params: ModelGenerateParams) {
  const input = [];

  if (params.systemPrompt) {
    input.push({
      role: "system",
      content: [{ type: "input_text", text: params.systemPrompt }]
    });
  }

  for (const message of params.messages) {
    input.push({
      role: mapMessageRole(message.role),
      content: [{ type: "input_text", text: message.content }]
    });
  }

  return input;
}

function buildChatMessages(params: ModelGenerateParams): Array<{
  role: "system" | "user" | "assistant";
  content: string;
}> {
  const messages: Array<{ role: "system" | "user" | "assistant"; content: string }> = [];

  if (params.systemPrompt) {
    messages.push({
      role: "system",
      content: params.systemPrompt
    });
  }

  for (const message of params.messages) {
    messages.push({
      role: mapMessageRole(message.role),
      content: message.content
    });
  }

  return messages;
}

function mapMessageRole(role: AgentMessage["role"]): "system" | "user" | "assistant" {
  if (role === "tool") {
    return "assistant";
  }

  return role;
}

function readOutputText(response: unknown): string {
  if (response && typeof response === "object" && "output_text" in response) {
    const outputText = (response as { output_text?: unknown }).output_text;
    if (typeof outputText === "string") {
      return outputText;
    }
  }

  throw new Error("OpenAI response did not contain output_text.");
}

function readChatCompletionText(response: unknown): string {
  if (!response || typeof response !== "object" || !("choices" in response)) {
    throw new Error("Provider response did not contain choices.");
  }

  const choices = (response as { choices?: Array<{ message?: { content?: unknown } }> }).choices;
  const content = choices?.[0]?.message?.content;

  if (typeof content === "string") {
    return content;
  }

  if (Array.isArray(content)) {
    return content
      .map((part) =>
        part && typeof part === "object" && "text" in part
          ? String((part as { text?: unknown }).text ?? "")
          : ""
      )
      .join("");
  }

  throw new Error("Provider response did not contain message content.");
}

function extractJsonBlock(value: string): string {
  const trimmed = value.trim();
  if (trimmed.startsWith("{") || trimmed.startsWith("[")) {
    return trimmed;
  }

  const fenced =
    trimmed.match(/```json\s*([\s\S]*?)```/i) ??
    trimmed.match(/```\s*([\s\S]*?)```/i);

  if (fenced?.[1]) {
    return fenced[1].trim();
  }

  throw new Error("Model output did not contain valid JSON.");
}

function matchesAny(value: string, patterns: string[]): boolean {
  return patterns.some((pattern) => value.includes(pattern));
}

function extractDirectoryEntries(data: unknown): Array<{ name: string; type: string }> {
  if (!data || typeof data !== "object" || !("entries" in data)) {
    return [];
  }

  const maybeEntries = (data as { entries?: unknown }).entries;
  if (!Array.isArray(maybeEntries)) {
    return [];
  }

  return maybeEntries.filter(isDirectoryEntry);
}

function isDirectoryEntry(value: unknown): value is { name: string; type: string } {
  if (!value || typeof value !== "object") {
    return false;
  }

  return "name" in value && "type" in value;
}

const jsonInstructionPrefix = [
  "Return valid JSON only.",
  "Do not wrap the JSON in markdown fences.",
  "Do not add any explanation before or after the JSON."
].join(" ");

const taskEntityRefSchema = {
  type: "object",
  properties: {
    kind: {
      type: "string",
      enum: ["file", "directory", "workspace", "text"]
    },
    path: { type: "string" },
    value: { type: "string" }
  },
  required: ["kind"],
  additionalProperties: false
} as const;

const taskConstraintSchema = {
  type: "object",
  properties: {
    kind: {
      type: "string",
      enum: ["workspace_only", "must_create_dirs", "must_exist", "single_replace"]
    },
    value: {
      anyOf: [{ type: "boolean" }, { type: "string" }]
    }
  },
  required: ["kind", "value"],
  additionalProperties: false
} as const;

const taskIntentStepSchema = {
  type: "object",
  properties: {
    type: {
      type: "string",
      enum: ["inspect", "read", "write", "patch", "analyze", "finish"]
    },
    description: { type: "string" },
    target: taskEntityRefSchema,
    params: {
      type: "object",
      additionalProperties: {
        anyOf: [{ type: "string" }, { type: "boolean" }, { type: "number" }]
      }
    }
  },
  required: ["type", "description"],
  additionalProperties: false
} as const;

const planningDecisionSchema = {
  type: "object",
  properties: {
    intent: {
      type: "object",
      properties: {
        type: {
          type: "string",
          enum: ["inspect", "read", "write", "patch", "analyze"]
        },
        summary: { type: "string" },
        target: taskEntityRefSchema,
        inputs: {
          type: "object",
          additionalProperties: {
            anyOf: [{ type: "string" }, { type: "boolean" }, { type: "number" }]
          }
        },
        constraints: {
          type: "array",
          items: taskConstraintSchema
        },
        expectedOutcome: { type: "string" },
        proposedSteps: {
          type: "array",
          items: taskIntentStepSchema
        }
      },
      required: ["type", "summary", "inputs", "constraints", "expectedOutcome", "proposedSteps"],
      additionalProperties: false
    },
    confidence: { type: "number" },
    reasoning: { type: "string" }
  },
  required: ["intent", "confidence", "reasoning"],
  additionalProperties: false
} as const;

const nextActionSchema = {
  oneOf: [
    {
      type: "object",
      properties: {
        type: {
          type: "string",
          const: "tool_call"
        },
        toolName: { type: "string" },
        input: {
          type: "object",
          additionalProperties: true
        },
        thought: { type: "string" }
      },
      required: ["type", "toolName", "input", "thought"],
      additionalProperties: false
    },
    {
      type: "object",
      properties: {
        type: {
          type: "string",
          const: "finish"
        },
        finalOutput: { type: "string" },
        thought: { type: "string" }
      },
      required: ["type", "finalOutput", "thought"],
      additionalProperties: false
    }
  ]
} as const;
